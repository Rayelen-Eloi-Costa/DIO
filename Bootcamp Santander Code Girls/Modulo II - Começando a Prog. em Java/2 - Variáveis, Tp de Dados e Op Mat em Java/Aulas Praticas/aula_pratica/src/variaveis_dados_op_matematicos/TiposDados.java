package variaveis_dados_op_matematicos;

/**
 * 
 * Classe de exemplo para o execício da Aula 2 de Variáveis, Tipos de Dados e Operadores Aritmétricos.
 *
 */

public class TiposDados {

	public static void main(String[] args) {
		
		byte b1 = 10;
		byte b2 = 20;
		
		short s1 = 20000;
		//short s2 = 40000;
		
		//int i1 = 10000000000;
		int i2 = 28500;
		
		long l1 = 1000000000000000000L;
		// Para demostrar que o valor é long colocamos L no final.
		long l2 = 2004005000500055000L;
		
		//float f1 = 4.5;
		float f2 = 99.84f;
		// Para demostrar que o valor é float colocamos f no final.
		
		double d1 = 85.69;
		double d2 = 99.84D;
		
		char c1 = 'a';
		//char c2 = 'Tu';
		char c3 = '\u0057';
		
		String st1 = "Fulano";
		String st2 = "Ciclano";
		String st3 = "Não importa que você vá devagar, contanto que você não pare.";
		
		String dt1 = "12/06/1990";
		
		boolean bo1 = true;
		boolean bo2 = false;
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(s1);
		System.out.println(i2);
		System.out.println(l1);
		System.out.println(l2);
		System.out.println(f2);
		System.out.println(d1);
		System.out.println(d2);
		System.out.println(c1);
		System.out.println(c3);
		System.out.println(st1);
		System.out.println(st2);
		System.out.println(st3);
		System.out.println(dt1);
		System.out.println(bo1);
		System.out.println(bo2);
	}

}
